// Function to get a random selection of questions for an episode
export function getRandomQuestions(episodeId, count = 5) {
  const allQuestions = quizQuestions[episodeId] || [];
  const shuffled = allQuestions.sort(() => 0.5 - Math.random());
  return shuffled.slice(0, Math.min(count, allQuestions.length));
}
